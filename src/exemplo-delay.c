#include <unistd.h>
#include <stdio.h>

void exemplo1() {
	printf("Delay de 5 segundos.\n");
	sleep(5);
	printf("Fim.\n");
}

void exemplo2() {
	printf("Contagem regressiva.\n");
	sleep(1);
	printf("3\n");
	sleep(1);
	printf("2\n");
	sleep(1);
	printf("1\n");
	sleep(1);
	printf("Fim.\n");
}

